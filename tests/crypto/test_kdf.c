    assert(result < 0);
    result = bn_hkdf_expand(tc1_prk, tc1_info, sizeof(tc1_info), NULL, sizeof(okm));
    assert(result < 0);
    result = bn_hkdf_expand(tc1_prk, tc1_info, sizeof(tc1_info), okm, 10000); /* Too long */
    assert(result < 0);
    printf("  HKDF-Expand invalid parameters: PASSED\n");
    
