    assert(result < 0);
    result = bn_hkdf(tc1_salt, sizeof(tc1_salt), tc1_ikm, sizeof(tc1_ikm), 
                    tc1_info, sizeof(tc1_info), okm, 10000); /* Too long */
    assert(result < 0);
    printf("  Combined HKDF invalid parameters: PASSED\n");
    
